class ValidationHelper {
  bool isEmailValid(String email) {
    // Basic email validation logic, you can adjust as needed
    return email.isNotEmpty && email.contains('@');
  }

  bool isPasswordValid(String password) {
    // Password validation criteria
    bool isLengthValid = password.length >= 8;
    bool isNumberValid = password.contains(RegExp(r'\d'));
    bool isSpecialCharValid =
        password.contains(RegExp(r'[!@#$%^&*(),.?":{}|<>]'));
    bool isUpperCaseAndLowerCaseValid = password.contains(RegExp(r'[A-Z]')) &&
        password.contains(RegExp(r'[a-z]'));

    return isLengthValid &&
        isNumberValid &&
        isSpecialCharValid &&
        isUpperCaseAndLowerCaseValid;
  }
}

